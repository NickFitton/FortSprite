function levelEffectsFor(sprite) {
  const levelEffectSets = {
    water: ['2 shield', '3 shield', '4 shield', '5 shield', '6 shield'],
    earth: ['10%', '12.5%', '15%', '17.5%', '20%'],
    fire: ['150 damage', '125 damage', '100 damage', '75 damage', '50 damage'],
    duck: ['2 shield', '3 shield', '4 shield', '6 shield', '8 shield'],
    ghost: ['3 sec', '3.5 sec', '4 sec', '4.5 sec', '5 sec'],
    dream: ['Random item', 'Better item', 'Better item', 'Better item', 'Legendary loot'],
    demon: ['10 healing', '15 healing', '20 healing', '25 healing', '30 healing'],
    punk: ['Unknown', 'Unknown', 'Unknown', 'Unknown', 'Unknown'],
    king: ['30 damage', '40 damage', '60 damage', '80 damage', '120 damage'],
    'zero-point': ['6 sec', '7 sec', '8 sec', '9 sec', '10 sec'],
    fishy: ['25% swim / 10% move', '50% swim / 20% move', '100% swim / 30% move', '150% swim / 40% move', '200% swim / 50% move'],
    striker: ['6 sec', '7 sec', '8 sec', '9 sec', '10 sec'],
    aura: ['175 damage', '150 damage', '125 damage', '100 damage', '75 damage'],
    boss: ['+5 health/shield', '+10 health/shield', '+15 health/shield', '+20 health/shield', '+25 health/shield'],
    grim: ['3 sec', '3.5 sec', '4 sec', '4.5 sec', '5 sec']
  };
  const effects = levelEffectSets[sprite.key];
  if (effects) {
    return effects.map((effect, index) => ({ level: index + 1, effect }));
  }

  if (sprite.unreleased) {
    return [
      { level: 1, effect: 'Effect details are not available yet.' },
      { level: 2, effect: 'Effect details are not available yet.' },
      { level: 3, effect: 'Effect details are not available yet.' },
      { level: 4, effect: 'Effect details are not available yet.' },
      { level: 5, effect: 'Effect details are not available yet.' }
    ];
  }

  return [
    { level: 1, effect: 'Effect details are not available yet.' },
    { level: 2, effect: 'Effect details are not available yet.' },
    { level: 3, effect: 'Effect details are not available yet.' },
    { level: 4, effect: 'Effect details are not available yet.' },
    { level: 5, effect: 'Effect details are not available yet.' }
  ];
}

