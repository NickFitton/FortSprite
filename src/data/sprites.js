export const sprites = [
  ...spriteTypes.flatMap((sprite) =>
    variants.flatMap((variant) => {
      const image = sprite.images[variant] ?? sprite.images.Base;
      if (!image) return [];

      return {
        id: `${sprite.key}-${variant.toLowerCase()}`,
        name: variant === 'Base' ? `${sprite.name} Sprite` : `${variant} ${sprite.name} Sprite`,
        family: sprite.name,
        key: sprite.key,
        variant,
        rarity: variant === 'Base' ? sprite.rarity : 'special',
        ability: sprite.ability,
        released: !sprite.unreleased && releasedVariants.includes(variant),
        hasExactImage: Boolean(sprite.images[variant]),
        image
      };
    })
  ),
  burntPeanutSprite
];

